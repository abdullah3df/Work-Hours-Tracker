export const getTranslator = (language) => {
  return (key, params = {}) => {
    let translation = (translations[language] && translations[language][key]) || translations.en[key] || String(key);
    for (const placeholder in params) {
      translation = translation.replace(`{${placeholder}}`, String(params[placeholder]));
    }
    return translation;
  };
};
